# 전체 점수 중 60점 미만인 과목의 개수를 계산하여 반환하는 함수
# input 사용 금지

def under_60(scores):
    cnt = 0
    for i in scores:
        if i < 60:
            cnt += 1
    return cnt

############## 테스트 코드 #################
print(under_60([30, 60, 90, 70])) # 1
print(under_60([0, 10, 20, 30, 40, 50])) # 6
print(under_60([50, 70, 50, 45, 80, 80])) # 3
