spectrum = int(input())

if 620 <= spectrum <= 780:
    print('Red')
elif 590 <= spectrum < 620:
    print('Orange')
elif 570 <= spectrum < 590:
    print('Yellow')
elif 495 <= spectrum < 570:
    print('Green')
elif 450 <= spectrum < 495:
    print('Blue')
elif 425 <= spectrum < 450:
    print('Indigo')
elif 380 <= spectrum < 425:
    print('Violet')
